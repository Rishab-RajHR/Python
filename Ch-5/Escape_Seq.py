# For new line we use escape sequence \n

a = "Basil is a good boy\nbut not a bad boy"

print(a)

# tab sequence character

b = "Alex is from\tKerala"

print(b)

# For double quotes

m = "Alex Pandian is from \"Tanjavur\""

print(m)

# For single quotes

t = 'Tovino Thomas is from \'Madurai\''

print(t)